public class IngenCalc extends BasicCalc{


    double operationSin(double num1)
    {
        return  Math.sin(num1);
    }
    double operationCos(double num1)
    {
        return Math.cos(num1);
    }
    double operationTan(double num1)
    {
        return  Math.tan(num1);
    }
    double operationAtan(double num1)
    {
        return Math.atan(num1);
    }
    double operationLog(double num1)
    {
        return  Math.log(num1);
    }
    double operationSqrt(double num1)
    {
        return Math.sqrt(num1);
    }
}
